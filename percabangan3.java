public class percabangan3 {
    public static void main(String[] args) {
        int nilai = 87;
        
        if (nilai >= 80) {
            System.out.println("nilai " + nilai + " mendapat nilai A ");
        } else if (nilai >= 70) {
            System.out.println("nilai " + nilai + " mendapat nilai B ");
        }
        else if (nilai >= 60) {
            System.out.println("nilai " + nilai + " mendapat nilai C ");
        }
        else if (nilai >= 50) {
            System.out.println("nilai " + nilai + " mendapat nilai D ");
        }
        else if (nilai >= 40) {
            System.out.println("nilai " + nilai + " mendapat nilai E ");
        }
    }
}
