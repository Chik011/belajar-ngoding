public class perulanganfor {
    public static void main(String[] args) {
        //for ascending
        System.out.println("for ascending");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        
        //for descending
        System.out.println("for descending");
        for (int i = 5; i >= 1; i--)
            System.out.println(i);
    }
}
